// Check if a word is valid (4-5 letters)
export function isValidWord(word: string): boolean {
  const normalized = word.toLowerCase().trim();
  if (normalized.length === 5) {
    return FIVE_LETTER_WORDS.has(normalized);
  }
  if (normalized.length === 4) {
    return FOUR_LETTER_WORDS.has(normalized);
  }
  return false;
}

// Get word length points
export function getWordPoints(word: string): number {
  const length = word.trim().length;
  if (length === 5) return 3;
  if (length === 4) return 1;
  return 0;
}

// Get random 5-letter words for puzzle generation
export function getRandomFiveLetterWords(count: number): string[] {
  const wordsArray = Array.from(FIVE_LETTER_WORDS);
  const shuffled = wordsArray.sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count).map(w => w.toUpperCase());
}
